    public AddFornecedor(String id_produto){
        initPage();
        jButton1.setVisible(false);
         pdao = new FornecedoreDAO();
         fornecedor = pdao.SelecionaPorID(id_produto);
        jTextFieldNomeF.setText(fornecedor.getNome());
        jFormattedTextFieldCNPJ.setText(fornecedor.getCnpj());
        jFormattedTextFieldTelefone.setText(fornecedor.getTelefone());
        jComboBoxStatusF.setSelectedItem(fornecedor.getStatus());
    }
    
    public void initPage() {
        initComponents();
        jFormattedTextFieldCNPJ.setFormatterFactory(Mascara.setMaskCNPJ());
        jFormattedTextFieldTelefone.setFormatterFactory(Mascara.setMaskTelFixo());
        this.Self = this;
        Eventos.setAcessibilidade(this);
        jTextFieldNomeF.requestFocus();

        this.addMouseListener(new MouseAdapter() {
            public void mouseClicked(MouseEvent me) {
                //Verificando se o botão direito do mouse foi clicado
                if ((me.getModifiers() & MouseEvent.BUTTON3_MASK) != 0) {
                    JPopupMenu menu = new JPopupMenu();
                    JMenuItem item = new JMenuItem("Clique-me");
                    menu.add(item);
                    JMenuItem item2 = new JMenuItem("Salvar");
                    menu.add(item2);

                    item.addActionListener(new ActionListener() {
                        public void actionPerformed(ActionEvent ae) {
                            JOptionPane.showMessageDialog(null, "Fui clicado !");
                        }
                    });

                    menu.show(Self, me.getX(), me.getY());
                }
            }
        });
        jTextFieldNomeF.setDocument(new LimitaQtdaCaracteres(254));
        Eventos.mudacorfoco(jTextFieldNomeF);
        Eventos.mudacorfoco(jFormattedTextFieldTelefone);
        Eventos.mudacorfoco(jFormattedTextFieldCNPJ);
    }

