            if (jTextFieldNomeF.getText().equals("")){
                jTextFieldNomeF.requestFocus();
                JOptionPane.showMessageDialog(null, "O campo Nome é obrigatório","Aviso",JOptionPane.WARNING_MESSAGE);
                return;
            }   
                if (jFormattedTextFieldCNPJ.getText().equals("________/__")){
                jFormattedTextFieldCNPJ.requestFocus();
                JOptionPane.showMessageDialog(null, "O campo CNPJ é obrigatório","Aviso",JOptionPane.WARNING_MESSAGE);
                return;
            }
            try {
                PreparedStatement pst = conecta.getConnection().prepareStatement("insert into Fornecedores(nome,cnpj,telefone,status)values(?,?,?,?)");
                pst.setString(1, jTextFieldNomeF.getText().replace("_", "").replace("/", "").trim());
                pst.setString(2, jFormattedTextFieldCNPJ.getText().replace("_", "").replace("/", "").trim());
                pst.setString(3, jFormattedTextFieldTelefone.getText().replace("_", "").replace("(", "").replace("-", "").replace(")", "").trim());
                pst.setInt(4, jComboBoxStatusF.getSelectedIndex());

                pst.executeUpdate();
                JOptionPane.showMessageDialog(rootPane, "Salvo com sucesso!");
            } catch (SQLException ex) {
                JOptionPane.showMessageDialog(rootPane, "Erro na inserção! \n ERRO:!" + ex + "  chame o tecnico!!");;
            }
              this.setVisible(false);
