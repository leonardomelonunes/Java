    private void jButtonEditarNFActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonEditarNFActionPerformed
        // TODO add your handling code here:
        
       int linha = jTableNotas.getSelectedRow();
        if (linha != -1){
            Object idNota = jTableNotas.getValueAt(linha, 0);
            if(Integer.parseInt(idNota.toString())!= -1){
               ADDNotasFiscais verNF = new ADDNotasFiscais(idNota.toString());
               verNF.setVisible(true);
            }
        }else{
        JOptionPane.showMessageDialog(null, "Selecione a linha que deseja editar");}
    }//GEN-LAST:event_jButtonEditarNFActionPerformed

