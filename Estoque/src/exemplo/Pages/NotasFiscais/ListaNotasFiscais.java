    public ListaNotasFiscais() {
        initComponents();
       NFDAO nfDAO= new NFDAO();
        ArrayList<ModelNF> NF = nfDAO.SelecionaTudo();
        
        //jTableProdutos
        String[] Colunas = {"id_nf","numero","serie","valor","data","id_fornecedor"};
        DefaultTableModel TableModel = new DefaultTableModel (null,Colunas);
                
        for (int i=0; i<NF.size(); i++){
            ModelNF TempNFs = (ModelNF) NF.get(i);
            Object [] Linhas = {TempNFs.getId_nf(),TempNFs.getNumero(),TempNFs.getSerie(),TempNFs.getValor(),TempNFs.getData(),TempNFs.getId_fornecedor()};
            TableModel.addRow(Linhas);
            
        }
            jTableNotas.setModel(TableModel);
    }

