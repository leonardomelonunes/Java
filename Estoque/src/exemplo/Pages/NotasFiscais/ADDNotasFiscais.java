    public void ListarProdutos(int idNota){
         String[] Colunas = {"ID_Produto","Nome","Unidade","Quantidade","Valor"};
        DefaultTableModel TableModel = new DefaultTableModel (null,Colunas);
               Nf_ProdutosDAO pDao = new Nf_ProdutosDAO();
               
               ArrayList<Nf_produtosModel> prods = pDao.SelecionaPorID(""+idNota);
        for (int i=0; i<prods.size(); i++){
            Nf_produtosModel TempNFs = (Nf_produtosModel) prods.get(i);
            Object [] Linhas = {TempNFs.getProduto().getId_produto(),TempNFs.getProduto().getNome(),TempNFs.getProduto().getUnidade(),TempNFs.getQuantidade(),TempNFs.getValor()};
            TableModel.addRow(Linhas);
            
        }
            jTableProdNF.setModel(TableModel);
    
    }

