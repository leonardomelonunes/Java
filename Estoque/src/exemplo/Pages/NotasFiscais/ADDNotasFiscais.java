    private void jButtonAdicionarNFActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonAdicionarNFActionPerformed
        // TODO add your handling code here:

        Color danger = new Color(214, 135, 135);
        Color success = new Color(135, 214, 137);
        Boolean validator = true;
 
        if (jTextFieldNome.getText().trim().length() > 0 && jTextFieldNome.getText().length() <= 254) {
            jTextFieldNome.setBackground(success);
        } else {
            jTextFieldNome.setBackground(danger);
            validator = false;
        }

//        if (jFormattedTextFieldData.getText().replace("_", "").replace("/", "").trim().length() > 0 && jFormattedTextFieldData.getText().length() <= 10) {
//            jFormattedTextFieldData.setBackground(success);
//        } else {
//            jFormattedTextFieldData.setBackground(danger);
//        }
        if (jFormattedTextFieldCNPJ.getText().replace("_", "").replace("/", "").trim().length() > 0 && jFormattedTextFieldCNPJ.getText().length() <= 11) {
            jFormattedTextFieldCNPJ.setBackground(success);
        } else {
            jFormattedTextFieldCNPJ.setBackground(danger);
            validator = false;
        }
        if (jTextFieldNumero.getText().length() > 0 && jTextFieldNumero.getText().length() <= 11) {
            jTextFieldNumero.setBackground(success);
        } else {
            jTextFieldNumero.setBackground(danger);
            validator = false;
        }
        if (jTextFieldNS.getText().length() > 0 && jTextFieldNS.getText().length() <= 45) {
            jTextFieldNS.setBackground(success);
        } else {
            jTextFieldNS.setBackground(danger);
            validator = false;
        }
        if (jTextFieldValor.getText().length() > 0 && jTextFieldValor.getText().length() <= 13) {
            jTextFieldValor.setBackground(success);
        } else {
            jTextFieldValor.setBackground(danger);
            validator = false;

        }
        
        if (validator == true){
            
        try {
                            PreparedStatement pst = conecta.getConnection().prepareStatement("insert into nf(numero,serie,valor,data,id_fornecedor)values(?,?,?,(select now()),?)",Statement.RETURN_GENERATED_KEYS);
                            pst.setString(1, jTextFieldNumero.getText());
                            pst.setString(2, jTextFieldNS.getText());
                            pst.setString(3, jTextFieldValor.getText());
                            pst.setInt(4, idF);
                            
                           
            
                            pst.executeUpdate();
                            ResultSet rs = pst.getGeneratedKeys();
                            if(rs.next()){
                                ultimoID = rs.getInt(1);
                            }
                            
                            JOptionPane.showMessageDialog(rootPane, "Nota Fiscal cadastrada com sucesso!");
                        } catch (SQLException ex) {
                            JOptionPane.showMessageDialog(rootPane, "Erro na inserção! \n ERRO:!" + ex + "  chame o tecnico!!");;
                        }
        }else{
            JOptionPane.showMessageDialog(null, "Preencha os campos obrigatorios que estão destacados em vermelho");
        }
        jButtonAdicionarNF.setEnabled(false);
    }//GEN-LAST:event_jButtonAdicionarNFActionPerformed

